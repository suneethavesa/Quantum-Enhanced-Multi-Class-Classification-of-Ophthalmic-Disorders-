class QuantumLayer(nn.Module):
    def __init__(self, n_qubits, n_layers):
        super().__init__()
        self.n_qubits = n_qubits
        self.n_layers = n_layers
        weight_shape = (n_layers, n_qubits)
        self.weights = nn.Parameter(torch.rand(weight_shape))
        self.dev = qml.device("default.qubit", wires=n_qubits)

        # Define quantum node
        @qml.qnode(self.dev, interface="torch")
        def circuit(inputs, weights):
            qml.AngleEmbedding(inputs, wires=range(n_qubits), rotation='Y')
            for l in range(n_layers):
                for q in range(n_qubits):
                    qml.RY(weights[l, q], wires=q)
                for q in range(0, n_qubits-1, 2):
                    qml.CZ(wires=[q, q+1])
                for q in range(1, n_qubits-1, 2):
                    qml.CZ(wires=[q, q+1])
            return [qml.expval(qml.PauliZ(q)) for q in range(n_qubits)]

        self.circuit = circuit

    def forward(self, x):
        batch_results = []
        for sample in x:
            out = self.circuit(sample, self.weights)
            batch_results.append(torch.tensor(out))
        return torch.stack(batch_results)

class QEyeNet(nn.Module):
    def __init__(self, n_classes=15):
        super().__init__()
        # Classical backbone
        self.backbone = models.resnet18(pretrained=True)
        self.backbone = nn.Sequential(*list(self.backbone.children())[:-1])
        for param in self.backbone.parameters():
            param.requires_grad = False

        # Quantum components
        self.reducer = nn.Linear(512, 12)  # Reduce to quantum input size
        self.quantum_layer = QuantumLayer(12, 8)
        self.classifier = nn.Linear(12, n_classes)

    def forward(self, x):
        x = self.backbone(x).squeeze()
        x = self.reducer(x)
        x = torch.tanh(x)  # Normalize to [-1, 1] for angle embedding
        x = self.quantum_layer(x)
        return self.classifier(x)

