class QuantumCircuitVisualizer:
    def __init__(self, n_qubits=12, n_layers=8):
        self.n_qubits = n_qubits
        self.n_layers = n_layers
        self.dev = qml.device("default.qubit", wires=n_qubits)

    def circuit(self, inputs, weights):
        # Angle embedding
        qml.AngleEmbedding(inputs, wires=range(self.n_qubits), rotation='Y')

        # Variational layers
        for layer in range(self.n_layers):
            # Rotation gates
            for qubit in range(self.n_qubits):
                qml.RY(weights[layer, qubit], wires=qubit)

            # Entanglement
            for i in range(0, self.n_qubits-1, 2):
                qml.CZ(wires=[i, i+1])
            for i in range(1, self.n_qubits-1, 2):
                qml.CZ(wires=[i, i+1])
            qml.Barrier(wires=range(self.n_qubits))

        # Measurement
        return [qml.expval(qml.PauliZ(i)) for i in range(self.n_qubits)]

    def draw_circuit(self):
        # Initialize weights
        weights = np.random.rand(self.n_layers, self.n_qubits)
        inputs = np.random.rand(self.n_qubits)

        # Create QNode and draw
        circuit = qml.QNode(self.circuit, self.dev)
        fig, ax = qml.draw_mpl(circuit)(inputs, weights)
        plt.title(f"Quantum Circuit: {self.n_qubits} Qubits, {self.n_layers} Layers")
        plt.tight_layout()
        plt.show()

        return fig

