class DataVisualizer:
    def __init__(self, class_names):
        rcParams['font.family'] = 'serif'
        rcParams['font.size'] = 10
        self.class_names = class_names
        self.colors = plt.cm.tab20(np.linspace(0, 1, len(class_names)))

    def plot_class_distribution(self, dataset):
        """Visualize class distribution with bar chart and pie chart"""
        class_counts = {name: 0 for name in self.class_names}

        # Count samples per class
        for _, label in dataset:
            class_counts[self.class_names[label]] += 1

        # Prepare data for plotting
        labels = list(class_counts.keys())
        counts = list(class_counts.values())

        # Create figure
        plt.figure(figsize=(15, 6))

        # Bar chart
        plt.subplot(1, 2, 1)
        bars = plt.bar(labels, counts, color=self.colors)
        plt.xlabel('Disease Classes')
        plt.ylabel('Number of Samples')
        plt.title('Class Distribution in Dataset')
        plt.xticks(rotation=45, ha='right')

        # Add counts on top of bars
        for bar in bars:
            height = bar.get_height()
            plt.annotate(f'{height}',
                         xy=(bar.get_x() + bar.get_width() / 2, height),
                         xytext=(0, 3),  # 3 points vertical offset
                         textcoords="offset points",
                         ha='center', va='bottom')

        # Pie chart
        plt.subplot(1, 2, 2)
        plt.pie(counts, labels=labels, autopct='%1.1f%%',
                colors=self.colors, startangle=90)
        plt.axis('equal')  # Equal aspect ratio ensures pie is circular
        plt.title('Class Proportion')

        plt.tight_layout()
        plt.show()

        return class_counts

    def visualize_class_samples(self, dataset, samples_per_class=3):
        """Show sample images for each disease class"""
        # Create grid of sample images
        fig, axes = plt.subplots(len(self.class_names), samples_per_class,
                                figsize=(15, 1.5*len(self.class_names)))
        fig.suptitle('Sample Images for Each Ophthalmic Condition', fontsize=16)

        # Collect samples for each class
        class_samples = {i: [] for i in range(len(self.class_names))}
        for image, label in dataset:
            if len(class_samples[label]) < samples_per_class:
                class_samples[label].append(image)

        # Plot images
        for class_idx, class_name in enumerate(self.class_names):
            for sample_idx in range(samples_per_class):
                ax = axes[class_idx, sample_idx]
                img = class_samples[class_idx][sample_idx].permute(1, 2, 0).numpy()
                ax.imshow(img)
                ax.axis('off')

                # Only add label to first column
                if sample_idx == 0:
                    ax.set_ylabel(class_name, fontsize=10)

        plt.tight_layout()
        plt.subplots_adjust(top=0.95)
        plt.show()

    def plot_feature_space(self, features, labels, method='PCA'):
        """Visualize feature space using dimensionality reduction"""
        from sklearn.manifold import TSNE
        from sklearn.decomposition import PCA

        plt.figure(figsize=(10, 8))

        # Apply dimensionality reduction
        if method == 'PCA':
            reducer = PCA(n_components=2)
            title = 'PCA Visualization of Feature Space'
        else:
            reducer = TSNE(n_components=2, perplexity=30)
            title = 't-SNE Visualization of Feature Space'

        reduced_features = reducer.fit_transform(features)

        # Plot each class separately
        for class_idx in range(len(self.class_names)):
            class_mask = (labels == class_idx)
            plt.scatter(
                reduced_features[class_mask, 0],
                reduced_features[class_mask, 1],
                color=self.colors[class_idx],
                label=self.class_names[class_idx],
                alpha=0.7,
                s=25
            )

        plt.title(title)
        plt.xlabel(f'{method} Component 1')
        plt.ylabel(f'{method} Component 2')
        plt.legend(bbox_to_anchor=(1.05, 1), loc='upper left')
        plt.grid(alpha=0.2)
        plt.tight_layout()
        plt.show()

    def plot_per_class_metrics(self, model, test_loader):
        """Visualize per-class performance metrics"""
        model.eval()
        all_preds = []
        all_labels = []

        with torch.no_grad():
            for images, labels in test_loader:
                images = images.to(next(model.parameters()).device)
                outputs = model(images)
                _, preds = torch.max(outputs, 1)
                all_preds.extend(preds.cpu().numpy())
                all_labels.extend(labels.numpy())

        # Calculate per-class metrics
        from sklearn.metrics import precision_recall_fscore_support
        precision, recall, f1, _ = precision_recall_fscore_support(
            all_labels, all_preds, average=None, zero_division=0
        )

        # Create radar chart
        fig = plt.figure(figsize=(12, 10))

        # Precision-Recall-F1 Scores
        ax1 = fig.add_subplot(211)
        width = 0.25
        x = np.arange(len(self.class_names))

        ax1.bar(x - width, precision, width, label='Precision', alpha=0.8)
        ax1.bar(x, recall, width, label='Recall', alpha=0.8)
        ax1.bar(x + width, f1, width, label='F1-Score', alpha=0.8)

        ax1.set_ylabel('Score')
        ax1.set_title('Per-Class Performance Metrics')
        ax1.set_xticks(x)
        ax1.set_xticklabels(self.class_names, rotation=45, ha='right')
        ax1.legend(loc='lower right')
        ax1.set_ylim([0, 1.1])
        ax1.grid(axis='y', alpha=0.3)

        # Confusion Matrix
        ax2 = fig.add_subplot(212)
        cm = confusion_matrix(all_labels, all_preds)
        sns.heatmap(cm, annot=True, fmt='d', cmap='Blues',
                    xticklabels=self.class_names,
                    yticklabels=self.class_names,
                    ax=ax2)
        ax2.set_xlabel('Predicted')
        ax2.set_ylabel('True')
        ax2.set_title('Confusion Matrix')
        plt.xticks(rotation=45, ha='right')
        plt.yticks(rotation=0)

        plt.tight_layout()
        plt.show()

        return precision, recall, f1

